d = {'a': 1, 'b': 2, 'c': 3}

# Accessing values using keys
print(d['a'])

# Checking if a key is in the dictionary
print('a' in d)

# Traversal
for key in d:
    print(key, d[key])

# Using keys() method
print(d.keys())

# Using values() method
print(d.values())

# Using items() method
print(d.items())
